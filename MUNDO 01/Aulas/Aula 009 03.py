frase = 'Curso em Video Python'

print(frase.replace('Python','Android'))
# variavel.replace('x','y') troca a string x para a y 

print(frase.upper())
# variavel.upper() coloca todos os carecteres para Maiusculo de dentro da String 

print(frase.lower())
# variavel.lower() coloca todos os carecteres para Minusculo de dentro da String 

print(frase.capitalize())
# variavel.capitalize() coloca todos os carecteres para Minusculo de dentro da String porém o Primeira altera para Maiuscula

print(frase.title())
# variavel.title() Analisa quantas palavras tem pelo espaço e coloca o primeiro caractere como Maiusculo e os outros Minusculo