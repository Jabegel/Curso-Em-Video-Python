frase = 'Curso em Video Python'

print(frase.split())
# variavel.split utiliza os espaços para separar as palavras da String e as coloca dentro de uma lista onde cada palavra tem uma numeração nova do split

print('-'.join(frase))
# '-'.join(variavel) pega a string separada e as junta fazendo os espaços serem '-' // Pode alterar o tipo de união, se quiser espaço normal só deixar ''